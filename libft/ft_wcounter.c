#include "libft.h"

size_t	ft_wcounter(char const *s, char c)
{
	size_t	i;
	size_t	j;

	i = 0;
	j = 0;
	while (s[i] != '\0')
	{
		if (s[i] != c)
			j++;
		while (s[i] != c && s[i + 1] != '\0')
			i++;
		i++;
	}
	return (j);
}
