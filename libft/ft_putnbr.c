#include "libft.h"

void	ft_putnbr(int n)
{
	char	s;

	if (n == -2147483648)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		n = n * -1;
		write(1, "-", 1);
	}
	if (n > 9)
		ft_putnbr(n / 10);
	s = (n % 10) + '0';
	ft_putchar(s);
}
